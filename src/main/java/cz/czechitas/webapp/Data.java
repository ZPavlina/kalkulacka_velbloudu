package cz.czechitas.webapp;

public class Data {

    private int vek;
    private int vyska;
    private String barvaOci;
    private String barvaVlasu;
    private String delkaVlasu;

    public int getVek() {
        return vek;
    }

    public void setVek(int newValue) {
        vek = newValue;
    }

    public int getVyska() {
        return vyska;
    }

    public void setVyska(int newValue) {
        vyska = newValue;
    }

    public String getBarvaOci() {
        return barvaOci;
    }

    public void setBarvaOci(String newValue) {
        barvaOci = newValue;
    }

    public String getBarvaVlasu() {
        return barvaVlasu;
    }

    public void setBarvaVlasu(String newValue) {
        barvaVlasu = newValue;
    }

    public String getDelkaVlasu() {
        return delkaVlasu;
    }

    public void setDelkaVlasu(String newValue) {
        delkaVlasu = newValue;
    }
}
